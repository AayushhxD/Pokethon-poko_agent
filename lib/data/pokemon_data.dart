  // Get GIF URL for a Pokemon
  static String getGifUrl(String pokemonName) {
    final id = _findPokemonId(pokemonName);
    if (id != null) {
      return 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/versions/generation-v/black-white/animated/$id.gif';
    }
    return '';
  }

  // Get PNG URL for a Pokemon
  static String getPngUrl(String pokemonName) {
    // Check special URLs first (for Mega/variant forms)
    final specialUrl = _findSpecialImageUrl(pokemonName);
    if (specialUrl != null) {
      return specialUrl;
    }

    final id = _findPokemonId(pokemonName);
    if (id != null) {
      return 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/official-artwork/$id.png';
    }
    return '';
  }

  // Helper method to find special image URL for variant forms
  static String? _findSpecialImageUrl(String pokemonName) {
    // Direct lookup
    if (specialImageUrls.containsKey(pokemonName)) {
      return specialImageUrls[pokemonName];
    }

    // Case-insensitive lookup
    final lowerName = pokemonName.toLowerCase();
    for (final entry in specialImageUrls.entries) {
      if (entry.key.toLowerCase() == lowerName) {
        return entry.value;
      }
    }

    // Normalized lookup (remove dashes/spaces)
    final normalizedName =
        pokemonName.replaceAll(RegExp(r'[-\s]'), '').toLowerCase();
    for (final entry in specialImageUrls.entries) {
      final normalizedKey =
          entry.key.replaceAll(RegExp(r'[-\s]'), '').toLowerCase();
      if (normalizedKey == normalizedName) {
        return entry.value;
      }
    }

    return null;
  }

  // Helper method to find Pokemon ID with flexible matching
  static int? _findPokemonId(String pokemonName) {
    // Direct lookup first
    if (pokemonIds.containsKey(pokemonName)) {
      return pokemonIds[pokemonName];
    }

    // Try case-insensitive lookup
    final lowerName = pokemonName.toLowerCase();
    for (final entry in pokemonIds.entries) {
      if (entry.key.toLowerCase() == lowerName) {
        return entry.value;
      }
    }

    // Try common variations (with/without spaces, dashes)
    final normalizedName =
        pokemonName.replaceAll(RegExp(r'[-\s]'), '').toLowerCase();
    for (final entry in pokemonIds.entries) {
      final normalizedKey =
          entry.key.replaceAll(RegExp(r'[-\s]'), '').toLowerCase();
      if (normalizedKey == normalizedName) {
        return entry.value;
      }
    }

    return null;
  }

